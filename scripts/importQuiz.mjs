// ─── 업로드 ─────────────────────────────────────────────────
const PART_ID = '1'

async function main() {
  console.log(`[importQuiz] Part ${PART_ID}에 ${ROWS.length}곡 업로드 시작...`)
  let ok = 0
  for (const [id, song, artist, lyrics, opts, correctId] of ROWS) {
    const idStr = String(id)
    const question = {
      id: idStr,
      song,
      artist,
      lyrics,
      options: opts.map((text, i) => ({ id: i + 1, text })),
      correctId,
    }
    try {
      await setDoc(doc(db, 'quizzes', PART_ID, 'questions', idStr), question)
      ok++
      process.stdout.write(`  ✓ ${idStr.padStart(2, '0')}  ${song} — ${artist}\n`)
    } catch (err) {
      process.stdout.write(`  ✗ ${idStr.padStart(2, '0')}  ${song} — ${err.message}\n`)
    }
  }
  console.log(`\n[importQuiz] 완료: ${ok}/${ROWS.length} 업로드됨`)
  process.exit(0)
}

main().catch(err => {
  console.error('[importQuiz] 실패:', err)
  process.exit(1)
})
